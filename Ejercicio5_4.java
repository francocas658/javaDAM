public class Ejercicio5_4 {
	public static void main (String[] args) {

		System.out.println ("Los numeros del 320 al 160 de 20 en 20: ");

		for (int i = 320; i >= 160; i -= 20) {
			System.out.println (i);
		}
	}
}
